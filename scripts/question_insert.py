async def save_questions(questions: list[str]) -> None:
    await Tortoise.init(
        db_url=f"mysql://{MYSQL_USER}:{MYSQL_PASSWORD}@{MYSQL_HOST}:{MYSQL_PORT}/{MYSQL_DB}",
        modules={"models": ["app.models.questions"]},
    )

    for q in questions:
        await Question.create(question_text=q)
    await Tortoise.close_connections()


load_dotenv()

MYSQL_HOST = os.getenv("MYSQL_HOST")
MYSQL_PORT = os.getenv("MYSQL_PORT")
MYSQL_USER = os.getenv("MYSQL_USER")
MYSQL_PASSWORD = os.getenv("MYSQL_PASSWORD")
MYSQL_DB = os.getenv("MYSQL_DB")

run_async(save_questions(questions))
